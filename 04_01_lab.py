#5 Armstrong number
def SumOfDigits(num):
  sumlist=[]
  n=len(str(num))
  for i in str(num):
    sumlist.append(int(i)**n)
  return sum(sumlist)

def ArmsCheck(num):
  if SumOfDigits(num)==num:
    return True 
  return False

