#2 Friendly pair
def SumOfDivisors(n): 
  div=[]
  i = 1
  while i <= n : 
    if (n % i==0): 
      div.append(i)

    i = i + 1
  return sum(div)

def friendly(a,b):
  if (SumOfDivisors(a)/a==SumOfDivisors(b)/b):
    return "Friendly Pair"
  return "Not Friendly Pair"

print(friendly(6,28))

